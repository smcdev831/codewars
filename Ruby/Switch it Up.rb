def switch_it_up(number)
  case number
  when 0
    return "Zero"
  when 1
    return "One"
  when 2
    return "Two"
  when 3
    return "Three"
  when 4
    return "Four"
  when 5
    return "Five"
  when 6
    return "Six"
  when 7
    return "Seven"
  when 8
    return "Eight"
  when 9
    return "Nine"
  end
end
